piano_keys = ["a", "b", "c", "d", "f", "g"]

# c, d, f
print(piano_keys[2:5])

# c to the end
print(piano_keys[2:])

# everything up to position 5
print(piano_keys[:5])

# Using increments, every other i.e.
print(piano_keys[2:5:2])

# All items, but every second
print(piano_keys[::2])

# Every item from backwards
print(piano_keys[::-1])


